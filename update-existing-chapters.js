async function updateExistingChapters() {
  console.log('🔍 Checking existing chapters in Firebase...');
  
  try {
    // Get all existing chapters (capital C)
    const chaptersRef = collection(db, 'Chapters');
    const snapshot = await getDocs(chaptersRef);
    
    console.log(`📚 Found ${snapshot.docs.length} existing chapters:`);
    snapshot.docs.forEach(doc => {
      console.log(`  - ${doc.id}: ${doc.data().name || doc.data().title || 'Unknown'}`);
    });
    
    console.log('\n🚀 Updating chapters with skillTags...');
    
    let updatedCount = 0;
    
    // Update each existing chapter with matching skillTags
    for (const doc of snapshot.docs) {
      const chapterData = doc.data();
      const chapterName = chapterData.name || chapterData.title || doc.id;
      
      // Find matching skillTags (try exact match first, then partial match)
      let skillTags = null;
      
      // Exact match
      if (chapterSkillTagsMap[chapterName]) {
        skillTags = chapterSkillTagsMap[chapterName];
      } else {
        // Partial match - check if any key contains the chapter name or vice versa
        for (const [key, tags] of Object.entries(chapterSkillTagsMap)) {
          if (key.toLowerCase().includes(chapterName.toLowerCase()) || 
              chapterName.toLowerCase().includes(key.toLowerCase())) {
            skillTags = tags;
            console.log(`  📝 Matched "${chapterName}" with "${key}"`);
            break;
          }
        }
      }
      
      if (skillTags) {
        try {
          await updateDoc(doc.ref, {
            'skillTags': skillTags,
            updatedAt: new Date()
          });
          console.log(`  ✅ Updated "${chapterName}" with ${skillTags.length} skillTags`);
          updatedCount++;
        } catch (error) {
          console.error(`  ❌ Failed to update "${chapterName}":`, error);
        }
      } else {
        console.log(`  ⚠️  No skillTags found for "${chapterName}"`);
      }
    }
    
    console.log(`\n🎉 Successfully updated ${updatedCount} chapters with skillTags!`);
    console.log('🤖 SkillTags are now ready for OpenAI GPT integration!');
    
  } catch (error) {
    console.error('❌ Error:', error);
  }
  
  process.exit(0);
}

// Run the update
updateExistingChapters().catch(console.error);